package com.learn2drive.util;

public enum FuelType {
	PETROL(250),DIESEL(1440),NATURAL_GAS(2500);
//object can't be created of enum
	
	
	public int CombustionTemperature;

	private FuelType(int combustionTemperature) {
	CombustionTemperature = combustionTemperature;
}

	public int getGetCombustionTemperature() {
		return CombustionTemperature;
	}

	public void setGetCombustionTemperature(int CombustionTemperature) {
		this.CombustionTemperature = CombustionTemperature;
	}
	
}
